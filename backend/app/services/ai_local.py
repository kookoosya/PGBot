def should_use_local_fallback(message: str) -> bool:
    """Локальные шаблоны — только для типичных вопросов о посёлке."""
    low = message.lower()
    return any(h in low for h in _VILLAGE_HINTS)


def local_chat_reply(message: str) -> str:
    msg = message.strip()
    low = msg.lower()

    if _has_any(low, ("привет", "здравств", "добрый", "хай", "hello")):
        return (
            "🪶 Здравствуйте! Я помощник портала Пушкинских Гор.\n\n"
            "Могу подсказать про посёлок, помочь с текстом объявления, идеями для дачи "
            "или маршрутом по Пушкиногорью. Спросите конкретнее — или откройте «Карту» и «Объявления» на сайте."
        )

    if _has_any(low, ("объявлен", "продам", "продаю", "куплю", "дрова", "ваканс")):
        return (
            "📋 Шаблон объявления:\n\n"
            f"{msg[:60] if len(msg) < 60 else 'Название товара/услуги'}\n"
            "Состояние: отличное\n"
            "Цена: договорная\n"
            "Район: Пушкинские Горы\n"
            "Телефон: ваш номер\n\n"
            "Разместить бесплатно: раздел «Объявления» на сайте или VK-бот."
        )

    if _has_any(low, ("посмотрет", "экскурс", "турист", "маршрут", "куда сходить", "достопримеч")):
        return (
            "🗺 Что посмотреть в Пушкиногорье:\n\n"
            "• Михайловское — усадьба и музей Пушкина\n"
            "• Святогорский монастырь и памятник на могиле поэта\n"
            "• Тригорское и Петровское — усадьбы заповедника\n"
            "• Центр посёлка — магазины, кафе, аптеки (см. «Карту»)\n\n"
            "На карте сайта — такси, гостиницы, АЗС с телефонами и маршрутами."
        )

    if _has_any(low, ("жалоб", "проблем", "жкх", "дорог", "освещ", "мусор", "яма")):
        return (
            "⚠️ Чтобы сообщить о проблеме официально:\n\n"
            "1. Откройте раздел «Жалобы» на сайте\n"
            "2. Укажите адрес, категорию и описание\n"
            "3. Или напишите в VK-бот портала\n\n"
            "Обращения попадают в систему «Народный контроль» для администрации и ЖКХ."
        )

    if _has_any(low, ("карт", "магазин", "аптек", "такси", "гостиниц", "азс", "кафе")):
        return (
            "🗺 Откройте «Карту» на сайте — там магазины, аптеки, такси, гостиницы, "
            "кафе и АЗС с рейтингами. Можно построить маршрут в Яндекс.Картах одной кнопкой. "
            "Справочник обновляется каждые 6 часов."
        )

    if _has_any(low, ("услуг", "мастер", "покос", "огород", "покрас", "ремонт", "снег")):
        return (
            "🛠 Раздел «Услуги» — мастера посёлка: огород, дрова, покос, ремонт и др. "
            "Мастера могут зарегистрироваться и вести записи в личном кабинете."
        )

    if _has_any(low, ("дач", "огород", "лето", "посадк")):
        return (
            "🌿 Идеи для дачи:\n"
            "• Ранний покос и прополка грядок до жары\n"
            "• Мульчирование — меньше поливов\n"
            "• Компост из травы и листьев\n"
            "• Объявление «ищу помощника» — в разделе «Объявления»\n"
            "• Мастера на покос — в «Услугах»"
        )

    if _has_any(low, ("пушкин", "поэт", "стих", "музей")):
        return (
            "🪶 Александр Пушкин жил в Михайловском (ныне музей-заповедник). "
            "«Здесь русский дух… здесь Русью пахнет!» — о Псковщине.\n\n"
            "Рекомендую: Михайловское, Тригорское, монастырь на Святой горе, "
            "литературные тропы и вид на Сороть."
        )

    if _has_any(low, ("картин", "изображ", "нарисуй", "фото")):
        return (
            "🎨 Перейдите во вкладку «Картинки» в ИИ-помощнике, выберите модель Flux "
            "и опишите сцену коротко (например: «зимняя изба, снег, вечер»). "
            "Если генерация не сработала — попробуйте через минуту или упростите описание."
        )

    # Общий ответ с контекстом вопроса
    short_q = msg[:200] + ("…" if len(msg) > 200 else "")
    return (
        f"🪶 Спасибо за вопрос!\n\n"
        f"По теме «{short_q}»:\n"
        f"{VILLAGE_INFO}\n\n"
        "Уточните запрос — например: «напиши объявление про дрова», "
        "«что посмотреть», «как подать жалобу». "
        "Для полноценного ИИ-диалога администратору нужен рабочий ключ Gemini в настройках сервера."
    )
